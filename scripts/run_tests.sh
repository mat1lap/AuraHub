#!/bin/bash
set -e # Остановить выполнение, если хоть один тест упадет

BUILD_DIR="build_userver2"

echo "--- 🔍 Проверка окружения ---"
if [ ! -d "$BUILD_DIR" ]; then
    echo "❌ Ошибка: Папка $BUILD_DIR не найдена."
    echo "Сначала запусти скрипт сборки (build_server.sh), чтобы подготовить зависимости!"
    exit 1
fi

echo "--- ⚙️ Конфигурация CMake (Включение тестов) ---"
# Переконфигурируем ту же папку, но теперь включаем тесты. 
# CMake достаточно умный, чтобы не пересобирать то, что уже собрано.
cmake -S . -B "$BUILD_DIR" -DAURAHUB_BUILD_TESTS=ON

echo "--- 🏗 Сборка тестовых таргетов ---"
# Собираем только то, что добавилось (сами тесты)
cmake --build "$BUILD_DIR" -j$(nproc)

echo "--- 🧪 Запуск тестов (ctest) ---"
# Флаг --output-on-failure покажет подробный лог только для упавших тестов, 
# чтобы не засорять консоль портянками текста от успешных.
ctest --test-dir "$BUILD_DIR" --output-on-failure

echo "--- ✅ Все тесты успешно пройдены! ---"